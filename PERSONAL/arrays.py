#Arrays 

#Lists
my_list = [1, 2, 3, 4, 5]
# ---> ordered, mutable, can contain duplicates

#Sets
my_set = {1, 2, 3, 4, 5}
# ---> unordered, mutable, cannot contain duplicates

#Tuples
my_tuple = (1, 2, 3, 4, 5)
# ---> ordered, immutable, can contain duplicates

#Dictionaries
my_dictionary = {
    "Name": "Mihai",
    "Age": 18,
    "City": "Chisinau"
}
# ---> unordered, mutable, can contain duplicates